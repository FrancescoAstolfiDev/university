package it.uniroma2.hstrain;

public class PassengersCarBean {
    private String idRef;
    private int seats;
    private String classPassengersCar;

    public String getIdRef() {
        return idRef;
    }

    public void setIdRef(String idRef) {
        this.idRef = idRef;
    }

    public int getSeats() {
        return seats;
    }

    public void setSeats(int seats) {
        this.seats = seats;
    }

    public String getClassPassengersCar() {
        return classPassengersCar;
    }

    public void setClassPassengersCar(String classPassengersCar) {
        this.classPassengersCar = classPassengersCar;
    }
}
